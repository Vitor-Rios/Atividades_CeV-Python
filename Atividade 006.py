# Digite um Valor e mostre o dobro, triplo e raiz quadrada

n1 = int(input(" Digite um Valor: "))
r1 = n1 * 2
r2 = n1 * 3
r3 = n1 ** 0.5

print(f'O Valor digitado foi {n1} \n'
      f'O dobro do valor digitado é: {r1} \n'
      f'O Triplo do valor digitado é: {r2} \n'
      f'A raiz quadrada do valor digitado é: {r3}')
